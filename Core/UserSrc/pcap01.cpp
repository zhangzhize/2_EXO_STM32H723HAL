PCAP01::PCAP01() {}

inline void PCAP01::csLow()
{
    if (_cs_port)
        HAL_GPIO_WritePin(_cs_port, _cs_pin, GPIO_PIN_RESET);
}

inline void PCAP01::csHigh()
{
    if (_cs_port)
        HAL_GPIO_WritePin(_cs_port, _cs_pin, GPIO_PIN_SET);
}

uint8_t PCAP01::spiReadWriteByte(uint8_t txdata)
{
    uint8_t rxdata = 0;
    HAL_SPI_TransmitReceive(_hspi, &txdata, &rxdata, 1, 100);
    return rxdata;
}

PCAP01_ERROR PCAP01::begin(SPI_HandleTypeDef *hspi, GPIO_TypeDef *cs_port, uint16_t cs_pin)
{
    if (!hspi || !cs_port)
        return PCAP01_ERROR::INIT_ERROR;

    _hspi = hspi;
    _cs_port = cs_port;
    _cs_pin = cs_pin;

    csHigh();

    sendOpcode(PCAP01_OPCODE_RESET);
    HAL_Delay(2);

    PCAP01_ERROR err = commTest();
    if (err != PCAP01_ERROR::NO_ERROR)
        return err;

    err = writeStdFirmware();
    if (err != PCAP01_ERROR::NO_ERROR)
        return err;

    writeStdConfig();
    
    writeRegister(PCAP01_RUNBIT_REG_ADDR, PCAP01_RUNBIT_REG_DATA);
    sendOpcode(PCAP01_OPCODE_PARTIAL_RESET);
    HAL_Delay(2);

    return PCAP01_ERROR::NO_ERROR;
}

void PCAP01::sendOpcode(uint8_t opcode)
{
    csLow();
    spiReadWriteByte(opcode);
    csHigh();
}

uint8_t PCAP01::sramComm(uint8_t comm_type, uint16_t addr, uint8_t data)
{
    uint8_t read_result = 0;

    uint8_t tx_buf[3];
    tx_buf[0] = comm_type | (addr >> 8);
    tx_buf[1] = addr & 0xFF;
    tx_buf[2] = (comm_type == PCAP01_SRAM_COMM_WRITE) ? data : 0xFF;

    uint8_t rx_buf[3] = {0};

    csLow();
    HAL_SPI_TransmitReceive(_hspi, tx_buf, rx_buf, 3, 100);
    csHigh();

    if (comm_type == PCAP01_SRAM_COMM_READ)
    {
        read_result = rx_buf[2];
    }

    return read_result;
}

void PCAP01::writeRegister(uint8_t reg_addr, uint32_t data)
{
    uint8_t tx_buf[4];
    tx_buf[0] = PCAP01_CMD_WRITE_REG | reg_addr;
    tx_buf[1] = (data >> 16) & 0xFF;
    tx_buf[2] = (data >> 8) & 0xFF;
    tx_buf[3] = data & 0xFF;

    csLow();
    HAL_SPI_Transmit(_hspi, tx_buf, 4, 100);
    csHigh();
}

uint32_t PCAP01::readRawRegister(uint8_t reg_addr)
{
    uint8_t tx_buf[4] = {(uint8_t)(PCAP01_CMD_READ_REG | reg_addr), 0xFF, 0xFF, 0xFF};
    uint8_t rx_buf[4] = {0};

    csLow();
    HAL_SPI_TransmitReceive(_hspi, tx_buf, rx_buf, 4, 100);
    csHigh();

    return (rx_buf[1] << 16) | (rx_buf[2] << 8) | rx_buf[3];
}

float PCAP01::readRegister(uint8_t reg_addr, uint8_t fractional_bits)
{
    uint32_t raw_data = readRawRegister(reg_addr);
    return (float)raw_data / (1 << fractional_bits);
}

void PCAP01::writeStdConfig(void)
{
    for (uint8_t i = 0; i < 11; i++)
    {
        writeRegister(i, PCap01_standard_config_reg[i]);
        HAL_Delay(1);
    }
}

PCAP01_ERROR PCAP01::commTest(void)
{
    uint8_t ret = 0x00;
    uint8_t retry_count = 0;

    while (ret != 0x55)
    {
        sramComm(PCAP01_SRAM_COMM_WRITE, 0x0D, 0x55);
        HAL_Delay(1);
        ret = sramComm(PCAP01_SRAM_COMM_READ, 0x0D, 0xFF);

        retry_count++;
        if (retry_count > 10)
        {
            return PCAP01_ERROR::COMM_TEST_FAILED;
        }
        HAL_Delay(50);
    }
    return PCAP01_ERROR::NO_ERROR;
}

PCAP01_ERROR PCAP01::writeStdFirmware(void)
{
    uint8_t ret = 0xBB;

    for (uint16_t addr = 0; addr < 4096; addr++)
    {
        uint8_t retry_count = 0;

        while (PCap01_standard_firmware[addr] != ret)
        {
            sramComm(PCAP01_SRAM_COMM_WRITE, addr, PCap01_standard_firmware[addr]);
            ret = sramComm(PCAP01_SRAM_COMM_READ, addr, 0xFF);

            retry_count++;
            if (retry_count > 5)
            {
                return PCAP01_ERROR::FIRMWARE_LOAD_FAILED;
            }
        }
        ret = 0xBB;
    }

    for (uint16_t addr = 0; addr < 30; addr++)
    {
        sramComm(PCAP01_SRAM_COMM_READ, addr, 0xFF);
    }

    return PCAP01_ERROR::NO_ERROR;
}